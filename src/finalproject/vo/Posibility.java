package finalproject.vo;

public class Posibility {
	private String posibility;
	private int count;
	
	public Posibility() {
		super();
	}
	
	public String getPosibility() {
		return posibility;
	}
	public void setPosibility(String posibility) {
		this.posibility = posibility;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	
}
